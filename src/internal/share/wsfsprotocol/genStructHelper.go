func genBytesCommentAndHelper(buf *bytes.Buffer) {
	bytesStructs := []string{}
	for _, typeName := range sortedStructNames {
		if structInfos[typeName].hasBytesField() {
			bytesStructs = append(bytesStructs, typeName)
		}
	}

	buf.WriteString("/*\nCommand structs with []byte payload fields:\n")
	if len(bytesStructs) == 0 {
		buf.WriteString("  (none)\n")
	} else {
		for _, typeName := range bytesStructs {
			if !strings.HasPrefix(typeName, "Cmd") {
				continue
			}
			buf.WriteString("  - " + typeName + "\n")
		}
	}
	buf.WriteString("*/\n\n")

	if len(bytesStructs) == 0 {
		return
	}

	buf.WriteString(`func readAllToBuffer(r io.Reader, dataBuf []byte) ([]byte, error) {
	n := 0
	for n < len(dataBuf) {
		readN, err := r.Read(dataBuf[n:])
		n += readN
		if err == io.EOF {
			return dataBuf[:n], nil
		}
		if err != nil {
			return nil, err
		}
	}

	var extra [1]byte
	readN, err := r.Read(extra[:])
	if readN > 0 {
		return nil, io.ErrShortBuffer
	}
	if err == io.EOF {
		return dataBuf[:n], nil
	}
	return nil, err
}

`)
}

func genHelper() error {
	writeTo := outputFile

	data, err := os.ReadFile(writeTo)
	if err != nil && !errors.Is(err, os.ErrNotExist) {
		return err
	}
	if err == nil {
		if !strings.Contains(string(data[:min(len(banner)+64, len(data))]), banner) {
			return fmt.Errorf("'%s' exists and not generated code", writeTo)
		}
	}

	var buf bytes.Buffer
	buf.WriteString("package " + packageName + "\n\n")

	if unsafeMode {
		buf.WriteString(`import "unsafe"`)
	} else {
		buf.WriteString(`import "encoding/binary"`)
	}
	buf.WriteString(`
		import (
			"io"
		)
	`)

	if unsafeMode {
		buf.WriteString(`
//Note: Unsafe mode assume that:
// 1. target is native little-endian
// 2. target have good profmance in unaligned memory access
// 3. Reader/Writer is buffered

`)
	}
	genBytesCommentAndHelper(&buf)

	err = genGetRequiredSize(&buf)
	if err != nil {
		return err
	}
	err = genWrite2Writer(&buf)
	if err != nil {
		return err
	}
	err = genReadFromReader(&buf)
	if err != nil {
		return err
	}
	err = genReadWithBufferFromReader(&buf)
	if err != nil {
		return err
	}

	formated, err := format.Source(buf.Bytes())
	if err != nil {
		os.WriteFile(writeTo+".debug", buf.Bytes(), 0644)
		return fmt.Errorf("format error: %v", err)
	}

	// bypass format to avoid some strange behavior from format.Source().
	buf.Reset()
	buf.WriteString(banner + "\n")
	if buildTag != "" {
		fmt.Fprintf(&buf, "//go:build %s\n", buildTag)
	}
	buf.WriteString("\n")
	buf.Write(formated)

	return os.WriteFile(writeTo, buf.Bytes(), 0644)
}

func main() {
	if len(os.Args) != 5 || (os.Args[1] != "0" && os.Args[1] != "1") {
		fmt.Println("Bad usage")
		fmt.Printf("Usage: %s UNSAFE_MODE(0/1) BUILD_TAG INPUT_FILE OUTPUT_FILE\n", os.Args[0])
		os.Exit(1)
	}
	inputFile = os.Args[3]
	outputFile = os.Args[4]
	buildTag = os.Args[2]
	if os.Args[1] == "1" {
		unsafeMode = true
	}

	err := parseStructInfos(inputFile)
	if err != nil {
		fmt.Println("Unable to parse file:", err)
		os.Exit(1)
	}

	for {
		if analyzeStructInfoPODSize() == 0 {
			break
		}
	}
	analyzeStructInfoBufSize()
	// for name, info := range structInfos {
	// 	fmt.Println(name, info.PODSize)
	// 	for k, v := range info.Fields {
	// 		fmt.Println("\t", k, v)
	// 	}
	// }
	for name, info := range structInfos {
		if info.PODSize < 0 {
			fmt.Printf("Unable to analyze the size of struct '%s'\n", name)
			os.Exit(1)
		}
	}

	err = genHelper()
	if err != nil {
		fmt.Printf("failed to generate: %v\n", err)
		os.Exit(1)
	}
}
